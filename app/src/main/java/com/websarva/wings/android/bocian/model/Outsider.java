package com.websarva.wings.android.bocian.model;

public class Outsider {
    private String companyID;
    private String companyName;
    private String outsiderID;
    private String outsiderName;
    private String ruby;
    private String positionName;

    public String getCompanyID() {
        return companyID;
    }

    public void setCompanyID(String companyID) {
        this.companyID = companyID;
    }

    public String getCompanyName() {
        return companyName;
    }

    public void setCompanyName(String companyName) {
        this.companyName = companyName;
    }

    public String getOutsiderID() {
        return outsiderID;
    }

    public void setOutsiderID(String outsiderID) {
        this.outsiderID = outsiderID;
    }

    public String getOutsiderName() {
        return outsiderName;
    }

    public void setOutsiderName(String outsiderName) {
        this.outsiderName = outsiderName;
    }

    public String getRuby() {
        return ruby;
    }

    public void setRuby(String ruby) {
        this.ruby = ruby;
    }

    public String getPositionName() {
        return positionName;
    }

    public void setPositionName(String positionName) {
        this.positionName = positionName;
    }
}
